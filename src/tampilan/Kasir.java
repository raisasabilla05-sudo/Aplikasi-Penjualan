    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
    int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data kasir ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
    
    if (ok == 0) {
        String sql = "DELETE FROM kasir WHERE id_kasir = ?"; 
        
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            
            int eksekusi = stat.executeUpdate();
            
            
            if (eksekusi > 0) {
                JOptionPane.showMessageDialog(null, "Data kasir berhasil dihapus");
                kosong();           
                txtid.requestFocus(); 
            } else {
                JOptionPane.showMessageDialog(null, "ID tidak ditemukan, data gagal dihapus");
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan database: " + e);
        }
        
        // 3. Refresh tabel agar data yang dihapus hilang dari layar
        datatable(); 
    }// TODO add your handling code here:
    }//GEN-LAST:event_bhapusActionPerformed


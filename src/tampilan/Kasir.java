    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
    
    String jenis = null;
    if (rlaki.isSelected()) {
        jenis = "Laki-Laki";
    } else if (rperempuan.isSelected()) {
        jenis = "Perempuan";
    }

    String sql = "INSERT INTO kasir VALUES (?,?,?,?,?,?,?)";
    
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtid.getText());
        stat.setString(2, txtnm.getText());
        stat.setString(3, jenis);
        stat.setString(4, txttelp.getText());
        stat.setString(5, cbagama.getSelectedItem().toString());
        stat.setString(6, txtalamat.getText()); // Variabel yang sudah kamu ganti tadi
        stat.setString(7, jPass.getText());
        
       
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Kasir Berhasil Disimpan");
        
        
        kosong();
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e);
    }
    datatable();     // TODO add your handling code here:
    }//GEN-LAST:event_bsimpanActionPerformed

